function [c, ceq] = constraints(x)
L = 14;
P = 6000;

o = P * ((6 * L) / (x(4) * (x(3)^2)));
pc = 64746.022 * (1 - (0.0282346 * x(3))) * x(3) * (x(4)^3);
fi = 2.1952 / ((x(3)^3) * x(4));

T1 = 1 / (sqrt(2) * x(1) * x(2));
R = sqrt((x(2)^2) + (x(1) + x(3))^2);
T2 = ((L + (x(2)/ 2)) * R) / (sqrt(2) * x(1) * x(3) * (((x(2)^2) / 3) + ((x(1) + x(3))^2)));
T = P * sqrt((T1^2) + (T2^2) + ((2 * T1 * T2 * x(2)) / R));

c(1) = x(1) - x(4);
c(2) = T - 13600;
c(3) = o - 30000;
c(4) = 6000 - pc;
c(5) = fi - 0.25;

ceq = [];
end